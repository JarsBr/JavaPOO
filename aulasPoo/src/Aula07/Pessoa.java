package Aula07;

public class Pessoa {
	private String nome, corDoCabelo, bioTipoString;
	private int idade;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getCorDoCabelo() {
		return corDoCabelo;
	}
	public void setCorDoCabelo(String corDoCabelo) {
		this.corDoCabelo = corDoCabelo;
	}
	
	public String getBioTipoString() {
		return bioTipoString;
	}
	public void setBioTipoString(String bioTipoString) {
		this.bioTipoString = bioTipoString;
	}
	
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	

}
